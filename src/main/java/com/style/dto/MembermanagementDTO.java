package com.style.dto;

public class MembermanagementDTO {
	private int num;
	private String mem_id;
	private String mem_nick;
	private String mem_pwd;
	private String mem_name;
	private String mem_addr;
	private String mem_postnum;
	private String mem_phone;
	private String mem_usertype;
	
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public String getMem_nick() {
		return mem_nick;
	}
	public void setMem_nick(String mem_nick) {
		this.mem_nick = mem_nick;
	}
	public String getMem_pwd() {
		return mem_pwd;
	}
	public void setMem_pwd(String mem_pwd) {
		this.mem_pwd = mem_pwd;
	}
	public String getMem_name() {
		return mem_name;
	}
	public void setMem_name(String mem_name) {
		this.mem_name = mem_name;
	}
	public String getMem_addr() {
		return mem_addr;
	}
	public void setMem_addr(String mem_addr) {
		this.mem_addr = mem_addr;
	}
	public String getMem_postnum() {
		return mem_postnum;
	}
	public void setMem_postnum(String mem_postnum) {
		this.mem_postnum = mem_postnum;
	}
	public String getMem_phone() {
		return mem_phone;
	}
	public void setMem_phone(String mem_phone) {
		this.mem_phone = mem_phone;
	}
	public String getMem_usertype() {
		return mem_usertype;
	}
	public void setMem_usertype(String mem_usertype) {
		this.mem_usertype = mem_usertype;
	}
	
	
}
